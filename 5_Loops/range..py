# range function in python
# syntax
# range(start,stop,step)

# range (start,stop)
# range (start,stop,step)
# range (stop)

# range (start)
for i in range(5):
    print(i,end=" ")

print()

# range (start,stop)
for i in range(5,10):
    print(i,end=" ")

print()

# range (start,stop,step)
for i in range(5,10,2):
    print(i,end=" ")