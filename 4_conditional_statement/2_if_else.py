# if else statement 
# if else statement is used to check the condition
# if the condition is true then it will execute the statement
# if the condition is false then it will execute the else statement
# syntax
# if condition:
#     statement
# else:
#     statement

num=int(input("Enter a number: "))
if num%2==0:
    print("Number is even" , num)
else:
    print(num,"Number is odd")